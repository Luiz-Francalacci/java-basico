
public class ScitchCase {

	public static void main(String[] args) {
		String sigla = "a";
		
		
		switch(sigla.toLowerCase()) {
		case "p":
			System.out.println("Pequeno");
			break;
		case "m":
			System.out.println("Medio");
			break;
		case "g":
			System.out.println("Grande");
			break;
		default:
			System.out.println("Indefinido");
			
		}
		
		

	}

}
