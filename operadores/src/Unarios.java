
public class Unarios {

	public static void main(String[] args) {
		
		int numero = 5;
		System.out.println(-numero);
		System.out.println(numero);
		
		numero = -numero;
		System.out.println(numero);
		
		System.out.println();
		
		int idade = 21;
		System.out.println(idade);
		
		idade ++;
		idade ++;
		System.out.println(idade);
		idade --;
		idade -= 5;
		System.out.println(idade);
		System.out.println(++idade);
		
		System.out.println();
		
		boolean varivel = true;
		
		System.out.println(!varivel);
		System.out.println(varivel);

		
	}

}
